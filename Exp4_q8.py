'''8.	Take two sets and apply various set operations on them :
S1 = {Red ,yellow, orange , blue }
S2 = {violet, blue , purple}
'''
s1 = {"Red", "yellow", "orange", "blue"}
s2 = {"violet", "blue", "purple"}

print("Union:", s1 | s2)
print("Intersection:", s1 & s2)
print("Difference (s1 - s2):", s1 - s2)
print("Difference (s2 - s1):", s2 - s1)
